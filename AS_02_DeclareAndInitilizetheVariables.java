package assignments;

/*
  Declare and initialize the variables for all datatypes
 */

public class AS_02_DeclareAndInitilizetheVariables {

	public static void main(String[] args) {

		byte age = 90;
		System.out.println(age);

		short salary = 25000;
		System.out.println(salary);

		int biggerSalary = 3857541;
		System.out.println(biggerSalary);

		long mobileNo = 7841857026l;
		System.out.println(mobileNo);

		float a = 25.63f;
		System.out.println(a);

		double pi = 3.14157487595;
		System.out.println(pi);

		char gender = 'F';
		System.out.println(gender);

		boolean answer = true;
		System.out.println(answer);

		String name = "Sarasvati Ingole";
		System.out.println(name);

	}

}