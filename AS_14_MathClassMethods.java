package assignments;

/*
WAP for Math Class methods   addExact(), subExact(), multiplyExact(),Min(),Max(),abs(),sqrt(),Math.PI;
 */
public class AS_14_MathClassMethods {

	public static void main(String[] args) {
System.out.println(Math.random());
		
		System.out.println(Math.addExact(50,80));
		System.out.println(Math.subtractExact(85, 60));
		
		System.out.println(Math.multiplyExact(45, 20));
		
		System.out.println(Math.min(52, 42));

		System.out.println(Math.max(43, 34));
		
		System.out.println(Math.abs(-85));
		
		System.out.println(Math.pow(10, 3));
		
		System.out.println(Math.sqrt(120));
		
		double value=Math.PI;
		System.out.println(value);
		
		
		double areaOfCircle=Math.PI*10*10;
		System.out.println(areaOfCircle);

	}

}
